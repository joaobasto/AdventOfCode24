package org.example.Day2505;

public class Range {
    private Long minLimit;
    private Long maxLimit;

    public Range(Long minLimit, Long maxLimit) {
        this.minLimit = minLimit;
        this.maxLimit = maxLimit;
    }

    public Long getMinLimit() {
        return minLimit;
    }

    public Long getMaxLimit() {
        return maxLimit;
    }
}
