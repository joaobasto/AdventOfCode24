package org.example.Day17;

public class PartialSolution {

    long result;

    int valueIterator;

    public PartialSolution(long result, int valueIterator) {
        this.result = result;
        this.valueIterator = valueIterator;
    }

    public long getResult() {
        return result;
    }

    public int getValueIterator() {
        return valueIterator;
    }
}
